public class Point {

    private Double pointX;
    private Double pointY;

    public Point(Double pointX, Double pointY) {
        this.pointX = pointX;
        this.pointY = pointY;
    }

    public Double getPointX() {
        return pointX;
    }

    public void setPointX(Double pointX) {
        this.pointX = pointX;
    }

    public Double getPointY() {
        return pointY;
    }

    public void setPointY(Double pointY) {
        this.pointY = pointY;
    }
}
